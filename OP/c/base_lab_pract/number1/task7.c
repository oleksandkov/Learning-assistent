/*
Завдання 7. Дано змінні x, y. З'ясувати, чи належить крапка з координатами (x, y) кільцю з центром на початку координат із зовнішнім радіусом 5 і внутрішнім радіусом 10.
*/
#include <stdio.h>
#include <math.h>
int main() {
    double x, y, r;
    printf("Введіть x та y: ");
    scanf("%lf %lf", &x, &y);
    r = sqrt(x*x + y*y);
    if (r > 5 && r < 10) {
        printf("Точка належить кільцю.\n");
    } else {
        printf("Точка не належить кільцю.\n");
    }
    return 0;
}
