/*
Завдання 22. З'ясувати, чи належить точка (x, y) кільцю з центром на початку координат із зовнішнім радіусом 15 і внутрішнім радіусом 30.
*/
#include <stdio.h>
#include <math.h>
int main() {
    double x, y, r;
    printf("Введіть x та y: ");
    scanf("%lf %lf", &x, &y);
    r = sqrt(x*x + y*y);
    if (r > 15 && r < 30) {
        printf("Точка належить кільцю.\n");
    } else {
        printf("Точка не належить кільцю.\n");
    }
    return 0;
}
