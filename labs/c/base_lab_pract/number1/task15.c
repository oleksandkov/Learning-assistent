/*
Завдання 15. Обчислення сили струму на ділянці з двома паралельно сполученими резисторами R1 і R2, якщо напруга U.
*/
#include <stdio.h>
int main() {
    double R1, R2, U, I;
    printf("Введіть R1, R2, U: ");
    scanf("%lf %lf %lf", &R1, &R2, &U);
    I = U / (R1 * R2 / (R1 + R2));
    printf("Сила струму = %.2lf\n", I);
    return 0;
}
