/*
Завдання 11. Дане ціле число від 1 до 9999. Вивести рядок –словесний опис числа.
*/
#include <stdio.h>
int main() {
    int n;
    printf("Введіть ціле число від 1 до 9999: ");
    scanf("%d", &n);
    if (n < 1 || n > 9999) {
        printf("Число поза діапазоном.\n");
        return 1;
    }
    int digits = (n < 10) ? 1 : (n < 100) ? 2 : (n < 1000) ? 3 : 4;
    printf("%s %sзначне число\n", (n % 2 == 0) ? "парне" : "непарне", (digits == 1) ? "одно" : (digits == 2) ? "дво" : (digits == 3) ? "три" : "чотири");
    return 0;
}
