

/*
Завдання 3. Напишіть програму, яка зчитує з клавіатури три числа та
перевіряє, чи утворюють вони арифметичну прогресію.
*/
#include<locale.h>
#include <stdio.h>

int main() {
	int a, b, c;
	printf("Введіть три числа: \n");
	scanf("%d %d %d", &a, &b, &c);

	if ((b - a) == (c - b)) {
		printf("Числа утворюють арифметичну прогресію.\n");
	} else {
		printf("Числа не утворюють арифметичну прогресію.\n");
	}

	return 0;
}

