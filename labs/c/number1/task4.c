
/*
Завдання 4. Напишіть програму, яка зчитує з клавіатури три числа та
перевіряє, чи утворюють вони геометричну прогресію.
*/

#include <stdio.h>

int main() {
    double a, b, c;
    printf("Введіть три числа: ");
    scanf("%lf %lf %lf", &a, &b, &c);
    if (a != 0 && b != 0 && c != 0 && (b / a) == (c / b)) {
        printf("Числа утворюють геометричну прогресію.\n");
    } else {
        printf("Числа не утворюють геометричну прогресію.\n");
    }
    return 0;
}
