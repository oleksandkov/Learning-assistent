/*
Завдання 19. Зчитати три числа та перевірити, чи утворюють вони зростаючу послідовність.
*/
#include <stdio.h>
int main() {
    int a, b, c;
    printf("Введіть три числа: ");
    scanf("%d %d %d", &a, &b, &c);
    if (a < b && b < c) {
        printf("Числа утворюють зростаючу послідовність.\n");
    } else {
        printf("Числа не утворюють зростаючу послідовність.\n");
    }
    return 0;
}
