/*
Завдання 18. Зчитати три числа та перевірити, чи утворюють вони спадну послідовність.
*/
#include <stdio.h>
int main() {
    int a, b, c;
    printf("Введіть три числа: ");
    scanf("%d %d %d", &a, &b, &c);
    if (a > b && b > c) {
        printf("Числа утворюють спадну послідовність.\n");
    } else {
        printf("Числа не утворюють спадну послідовність.\n");
    }
    return 0;
}
